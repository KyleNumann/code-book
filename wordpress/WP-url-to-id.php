function wp_url_to_id($urls) {
	$ids = '';
	foreach($urls as $url){

		$thisID = url_to_postid( $url );

		if($thisID === 0){
			$urlParts = explode('/', $url);
			$urlEnd = $urlParts[count($urlParts)];
			$urlSecondLast = $urlParts[(count($urlParts) - 1)];
			echo 'no post ID, url END: '. $urlEnd .' and SecondLast:'. $urlSecondLast;
		}

		$ids .= "\n" . $thisID;
	}
	echo '<div class="postids" style="display:none;"><pre>';
	echo $ids;
	echo '</pre></div>';
}

